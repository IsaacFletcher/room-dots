#!/bin/bash

# Define paths
WP_DIR="$HOME/Pictures/Wallpapers"
#MAKO_CONF="$HOME/.config/mako/config"
#MAKO_TPM="$HOME/.cache/wal/mako-config"

# Ensure the wallpaper directory exists
mkdir -p "$WP_DIR"

# Logic to pick the wallpaper
if [ "$1" = "random" ]; then
    # Pick a random file from the directory
    WALLPAPER=$(find "$WP_DIR" -type f | shuf -n 1)

elif [ -f "$1" ]; then
    # Use the file passed as an argument
    WALLPAPER="$1"
else
    # Default to the current/last wallpaper
    WALLPAPER=$(cat "$HOME/.cache/wal/wal")
fi

# 1. Set the wallpaper with swww
swww img "$WALLPAPER" --transition-type center --transition-step 90 --transition-fps 60

# 2. Generate the colors with pywal
# --skip-16 keeps the terminal colors clean while generating the palette
wal -i "$WALLPAPER"
mkdir -p ~/.config
cp "$HOME/.cache/wal/colors-kde" "$HOME/.config/kdeglobals"

# 3. Update Mako Config
# Link the template generated by wal to mako's config path
kvantummanager --set PywalModern
#ln -sf "$MAKO_TPM" "$MAKO_CONF"
#sed -i "s/base = \"#[A-Fa-f0-9]*\" # Matches your @active-background/base = \"$color3\" # Matches your @active-background/" ~/.config/ashell/config.toml

# 4. Reload Services
#makoctl reload
swaync-client --reload-config
killall waybar
waybar &

# Optional: Send a notification to see the new theme
notify-send "Theme Updated" "Wallpaper: $(basename "$WALLPAPER")" -i "$WALLPAPER"
